#include <iostream>
using namespace std;

int main()
{
    int i;
    cout << " \n \n Printing first 10 natural numbers: \n"
         << endl;
    cout << "-------------------------------------\n"
         << endl;
    cout << "The natural numbers are: \n" << endl;
    for (i = 1; i <= 10; i++)
    {
        cout  << i << "  ";
    }
    cout << endl;
    return 0;
}